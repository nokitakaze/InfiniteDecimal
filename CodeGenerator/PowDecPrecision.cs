    public static Task CalculatePowDec()
    {
        var testValues = GetValues();

        foreach (var (based, exponent, expected) in testValues)
        {
            var foundAdd = -1;
            for (var add = 0; add < 50; add++)
            {
                var actual = based
                    .WithPrecision(PickedPrecision + add)
                    .Pow((-exponent).WithPrecision(PickedPrecision + add))
                    .Round(PickedPrecision);
                var lg10 = GetLg10Ceiling(actual);

                var localPrecision = (PickedPrecision - 2) - lg10;
                var epsilon = BigDec.PowFracOfTen(localPrecision);
                if ((actual >= expected - epsilon) && (actual <= expected + epsilon))
                {
                    foundAdd = add;
                    break;
                }
            }

            if (foundAdd == -1)
            {
                throw new Exception($"Невозможно найти решение для ({based}, {exponent}, {expected})");
            }

            Console.WriteLine("{0}; {1} => {2}", based, exponent, foundAdd);
        }

        throw new NotImplementedException();
    }
