    public static Task CalculatePowDec()
    {
        var testValues = GetValues();

        var prevWasShort = false;
        foreach (var (based, exponent, expected) in testValues)
        {
            /*
            Console.WriteLine("__{0}\t{1}", based, exponent);
            if ((based == 0.001) && (exponent == 20))
            {
            }
            */

            /*
            if (based <= 1)
            {
                continue;
            }
            */

            var foundAdd = -1;
            for (var add = 0; add < 50; add++)
            {
                var actual = based
                    .WithPrecision(PickedPrecision + add)
                    .Pow((-exponent).WithPrecision(PickedPrecision + add))
                    .Round(PickedPrecision);
                var lg10 = GetLg10Ceiling(actual);
                var localPrecision = Math.Min((OriginPrecision - 2) - lg10, PickedPrecision);
                var epsilon = BigDec.PowFracOfTen(localPrecision);
                if ((actual >= expected - epsilon) && (actual <= expected + epsilon))
                {
                    foundAdd = add;
                    break;
                }
            }

            if (foundAdd == -1)
            {
                throw new Exception($"Невозможно найти решение для ({based}, {exponent}, {expected})");
            }
            else if (foundAdd > 0)
            {
            }

            if (foundAdd > 0)
            {
                Console.WriteLine("{3}{0,11}\t{1,-15} => {2}",
                    based, exponent, foundAdd, prevWasShort ? "\n" : "");
                prevWasShort = false;
            }
            else
            {
                // Console.WriteLine("\t{0}; {1}", based, exponent);
                Console.Write(".");
                prevWasShort = true;
            }
        }

        return Task.CompletedTask;
    }
